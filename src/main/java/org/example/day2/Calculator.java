public class Calculator {
    public int add(int val1, int val2){
        int sum = val1 + val2;
        return sum;
    }

    public double add(double val1, int val2){
        double sum = val1 + val2;
        return sum;
    }

    public double add(double val1, double val2){
        double sum = val1 + val2;
        return sum;
    }

    public double add(int val1, double val2){
        double sum = val1 + val2;
        return sum;
    }

    public String add(String val1, String val2){
        String sum = val1 + val2;
        return sum;
    }

}
